package sample.Instructions;

/**
 * Instructions which can be used.
 */
public enum InstructionType {
    /**
     * A line instruction.
     */
    LINE,

    /**
     * A plot instruction.
     */
    PLOT,

    /**
     * A rectangle instruction.
     */
    RECTANGLE,

    /**
     * A ellipse instruction.
     */
    ELLIPSE,

    /**
     * A polygon instruction.
     */
    POLYGON,

    /**
     * A pen instruction.
     */
    PEN,

    /**
     * A fill instruction.
     */
    FILL
}
